			<blockquote><strong>文明上网七条底线</strong></blockquote>
			<p> 根据我国网络发展的现状，国信办鲁炜主任提出了网络空间的“七条底线”，我们每位网民在自由表达自己意见和诉求的同时，也要遵守“七条底线”，文明上网，争做文明网民。 </p>
			<p><strong>七条底线</strong><br />
			&nbsp;&nbsp;&nbsp;&nbsp;1.法律法规底线：有法可依、有法必依、执法必严、违法必究，任何时候，无论是网上网下，都将始终做到违法必究；<br />
			&nbsp;&nbsp;&nbsp;&nbsp;2.社会主义制度底线，为我们全面建成小康社会提供了有力地制度保障，我们要积极拥护社会主义及社会主义制度；<br />
			&nbsp;&nbsp;&nbsp;&nbsp;3.国家利益底线，作为国家公民，时刻维护我们伟大祖国的利益，这也是宪法赋予我们每位公民的光荣义务；<br />
			&nbsp;&nbsp;&nbsp;&nbsp;4.公民合法权益底线，我们在网络反腐的同时，切忌不能以“艳照”等不健康、不正当甚至违法手段对别人进行，否则不仅触犯法律，也侵犯了无辜者的合法权益；<br />
			&nbsp;&nbsp;&nbsp;&nbsp;5.社会公共秩序底线，网络世界必须也要遵循一定的秩序规则，唯有如此大家才能营造一个良好健康的网络环境；<br />
			&nbsp;&nbsp;&nbsp;&nbsp;6.道德风尚底线，崇尚美德在我国延续几千年的优秀传统，网络空间里也要讲道德，不做有违道德之事；<br />
			&nbsp;&nbsp;&nbsp;&nbsp;7.信息真实性底线，要求我们在上网时一定要实事求是，而不能以讹传讹、散发谣传，积极宣传政府部门发布的真实信息。</p>
			<p> 七条底线是根本，不能突破；是方圆，不能逾越。我们广大网民积极宣传落实“七条底线”，争做文明网民，净化网络空间，还网络一片“蓝天”。</p>
			
