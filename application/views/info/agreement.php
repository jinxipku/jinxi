  			<strong>第3条</strong> 今昔网所有权、经营权、管理权均属今昔网。<br />
			<strong>第4条</strong> 本协议最终解释权归属今昔网。</p>
			<blockquote><strong>第二章 今昔网用户</strong></blockquote>
			<p><strong>第5条</strong> 凡是注册用户和浏览用户均为今昔网用户（以下统称&quot;用户&quot;）。<br />
			<strong>第6条</strong>用户的个人信息受到保护，不接受任何个人或单位的查询。国家机关依法查询除外，用户的个人设置公开除外。<br />
			<strong>第7条</strong>用户享有言论自由的权利。<br />
			<strong>第8条</strong>用户的言行不得违反《计算机信息网络国际联网安全保护管理办法》、《互联网信息服务管理办法》、《互联网电子公告服务管理规定》、《维护互联网安全的决定》、《互联网新闻信息服务管理规定》等相关法律规定，不得在今昔网发布、传播或以其它方式传送含有下列内容之一的信息：<br />
			&nbsp;&nbsp;&nbsp;&nbsp;1.反对宪法所确定的基本原则的；<br />
			&nbsp;&nbsp;&nbsp;&nbsp;2.危害国家安全，泄露国家秘密，颠覆国家政权，破坏国家统一的；<br />
			&nbsp;&nbsp;&nbsp;&nbsp;3.损害国家荣誉和利益的；<br />
			&nbsp;&nbsp;&nbsp;&nbsp;4.煽动民族仇恨、民族歧视、破坏民族团结的；<br />
			&nbsp;&nbsp;&nbsp;&nbsp;5.破坏国家宗教政策，宣扬邪教和封建迷信的；<br />
			&nbsp;&nbsp;&nbsp;&nbsp;6.散布谣言，扰乱社会秩序，破坏社会稳定的；<br />
			&nbsp;&nbsp;&nbsp;&nbsp;7.散布淫秽、色情、赌博、暴力、凶杀、恐怖或者教唆犯罪的；<br />
			&nbsp;&nbsp;&nbsp;&nbsp;8.侮辱或者诽谤他人，侵害他人合法权利的；<br />
			&nbsp;&nbsp;&nbsp;&nbsp;9.煽动非法集会、结社、游行、示威、聚众扰乱社会秩序的；<br />
			&nbsp;&nbsp;&nbsp;&nbsp;10.以非法民间组织名义活动的；<br />
