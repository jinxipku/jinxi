			&nbsp;&nbsp;&nbsp;&nbsp;12.含有中国法律、法规、规章、条例以及任何具有法律效力之规范所限制或禁止的其它内容的。<br />
			<strong>第9条</strong>用户在今昔网发表文章时，除遵守本协议外，还需遵守吧主对该吧的相关规定。<br />
			<strong>第10条</strong> 用户不得在今昔网内发布任何形式的广告，包括但不限于：<br />
			&nbsp;&nbsp;&nbsp;&nbsp;1.发布带有任何有联系方式的网络创业、网络兼职、刷Q币以及非真实性中奖信息；<br />
			&nbsp;&nbsp;&nbsp;&nbsp;2.内容包含带有任何联系方式的学历职称代办，专业代做试题，作业，论文等题目以及售卖考试答案等信息；<br />
			&nbsp;&nbsp;&nbsp;&nbsp;3.发布任何带有联系方式的银行卡代办、买卖发票、非法彩票等信息；<br />
			&nbsp;&nbsp;&nbsp;&nbsp;4.含有联系方式的黑客、收费删帖、办证刻章等违法信息；<br />
			&nbsp;&nbsp;&nbsp;&nbsp;5.非官方认证的发布任何带有联系方式的医院、美容、药品、祛斑、医生专家和整容等信息。<br />
